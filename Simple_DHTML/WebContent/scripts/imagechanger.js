function shrinkImage(){
	document.getElementById("dogimage").style.height="50px";
	document.getElementById("dogimage").style.width="50px";
}
function enlargeImage(){
	document.getElementById("dogimage").style.height="200px";
	document.getElementById("dogimage").style.width="200px";
}
function resetImage(){
	document.getElementById("dogimage").style.height="100px";
	document.getElementById("dogimage").style.width="100px";
}